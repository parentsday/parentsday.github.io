import turtle
t = turtle.Turtle()
t.speed(5) # 1:slowest, 3:slow, 5:normal, 10:fast, 0:fastest

# Outline
t.up()
t.goto(-150, 0)
t.down()
t.forward(300)
t.right(90)
t.forward(150)
t.right(90)
t.forward(300)
t.right(90)
t.forward(150)
t.up()

# Red stripe
t.fillcolor('red')
t.begin_fill()
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.end_fill()
t.back(50)

# White stripe (optional as background is white)
t.fillcolor('white')
t.begin_fill()
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.end_fill()
t.back(50)

# Blue stripe
t.fillcolor('blue')
t.begin_fill()
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.right(90)
t.forward(300)
t.right(90)
t.forward(50)
t.end_fill()
